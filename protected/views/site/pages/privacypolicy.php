<h1>Privacy Policy</h1>

<p>
	KDE Identity considers the privacy of the information stored in it to be very important.<br />
	However, in order to allow you to use Identity login enabled sites, some information must be shared with those sites.
	This usually includes your username, full name and primary email address. 
	The shared information may be stored by the site in question, and it may provide access to it under different conditions.<br />
	<br />
	Information will be made accessible to others by Identity itself based on both your group membership and their group membership.
	Access to information compounds, so a person may recieve access to information from multiple groups they are members of.
</p>
<h4>Members of the 'users' group may access:</h4>
<ul>
	<li>The username, full name and primary email address of members of the developers group.</li>
</ul>
<h4>Members of the 'developers' or 'disabled-developers' groups may access:</h4>
<ul>
	<li>The username, full name, gender, current time, email addresses, jabber addresses, IRC nicknames and websites of members of the developers group.</li>
</ul>
<h4>Members of the 'ev-members' group may access:</h4>
<ul>
	<li>The username, full name, gender, date of birth, current time, e.V. membership status, home address, phone numbers, email addresses, jabber addresses, IRC nicknames and websites of members of the ev-members group.</li>
</ul>
<h4>Members of the 'ev-board' group may access:</h4>
<ul>
	<li>The username, full name and primary email address of all users of KDE Identity. This is for administrative purposes only, to allow for administering the membership of the KDE e.V.</li>
</ul>
<h4>Members of the 'sysadmins' group may access:</h4>
<ul>
	<li>All information stored by all users of KDE Identity. This is for administrative purposes, and is necessary to operate the site.</li>
</ul>
